'use strict';
const пары = [[1, 2], [3, 4], [5, 6]];
for (let [а, б] of пары) {
  console.log(а + "," + б);
}

const точки = [{ х: 10, у: 20 }, { х: 30, у: 40 }];
for (const { х, у } of точки) {
  console.log(х + ":" + у);
}

for (let [первый, ...хвост] of [[1, 2, 3, 4]]) {
  console.log(первый + " / " + хвост.length);
}

for (let [а = 100, б = 200] of [[1], [], [7, 8]]) {
  console.log(а + "-" + б);
}

for (let { к: { л } } of [{ к: { л: 42 } }, { к: { л: 99 } }]) {
  console.log("вложенный " + л);
}

const замыкания = [];
for (let [к, зн] of [["a", 1], ["b", 2], ["c", 3]]) {
  замыкания.push(() => к + "=" + зн);
}
console.log(замыкания[0]() + " " + замыкания[1]() + " " + замыкания[2]());

let аккум = 0;
for (let [а, б] of [[1, 1], [2, 2], [3, 3], [9, 9]]) {
  if (а == 3) { continue; }
  if (а == 9) { break; }
  аккум += а + б;
}
console.log("аккум=" + аккум);

function* ген() {
  for (let [а, б] of [[1, 2], [3, 4], [5, 6]]) {
    yield а + б;
  }
}
let сумма = 0;
for (let х of ген()) {
  сумма += х;
}
console.log("генсумма=" + сумма);
